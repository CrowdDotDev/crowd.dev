<template>
  <div>
    <el-dropdown
      v-if="hasPermission(LfPermission.noteEdit) || hasPermission(LfPermission.noteDestroy)"
      placement="bottom-end"
      trigger="click"
      @command="handleCommand"
      @visible-change="dropdownVisible = $event"
    >
      <button
        class="el-dropdown-link btn p-1.5 rounder-md hover:bg-gray-200 text-gray-600"
        type="button"
        @click.stop
      >
        <i class="text-xl ri-more-fill" />
      </button>
      <template #dropdown>
        <el-dropdown-item
          v-if="hasPermission(LfPermission.noteEdit)"
          command="noteEdit"
        >
          <i class="ri-pencil-line text-gray-400 mr-1" />
          <span>Edit note</span>
        </el-dropdown-item>
        <el-dropdown-item
          v-if="hasPermission(LfPermission.noteDestroy)"
          command="noteDelete"
          divided="divided"
        >
          <i class="ri-delete-bin-line text-red-500 mr-1" />
          <span class="text-red-500">Delete note</span>
        </el-dropdown-item>
      </template>
    </el-dropdown>
  </div>
</template>

<script setup>
import {
  ref,
} from 'vue';
import ConfirmDialog from '@/shared/dialog/confirm-dialog';
import { NoteService } from '@/modules/notes/note-service';
import Message from '@/shared/message/message';
import usePermissions from '@/shared/modules/permissions/helpers/usePermissions';
import { LfPermission } from '@/shared/modules/permissions/types/Permissions';

const emit = defineEmits(['edit', 'reload']);

const props = defineProps({
  note: {
    type: Object,
    required: true,
  },
});

const { hasPermission } = usePermissions();

const dropdownVisible = ref(false);

const doDestroyWithConfirm = () => {
  ConfirmDialog({
    icon: 'ri-delete-bin-line',
    type: 'danger',
    title: 'Delete note',
    message:
      'Are you sure you want to proceed? You canâ€™t undo this action',
    confirmButtonText: 'Confirm',
    cancelButtonText: 'Cancel',
  })
    .then(() => NoteService.destroyAll([props.note.id]))
    .then(() => {
      Message.success('Note successfully deleted!');
      emit('reload');
    });
};

const handleCommand = (command) => {
  if (command === 'noteDelete') {
    return doDestroyWithConfirm();
  } if (command === 'noteEdit') {
    emit('edit');
  }
  return null;
};
</script>

<script>
export default {
  name: 'AppNoteDropdown',
};
</script>
